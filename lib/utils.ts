export function getBannerClassName(name: string) {
  switch (name) {
    case 'hands holding':
      return 'handsHolding'

    case 'hands':
      return 'hands'

    case 'granny':
      return 'granny'

    case 'father':
      return 'father'

    case 'family field':
      return 'familyField'

    case 'two women':
      return 'twoWomen'

    case 'pregnant woman':
      return 'pregnantWoman'
  }
}
